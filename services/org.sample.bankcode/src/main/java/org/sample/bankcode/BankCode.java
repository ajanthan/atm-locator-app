package org.sample.bankcode;

public class BankCode {
    private String code;;
    private String zip;

    public BankCode(String code, String zip) {
        super();
        this.code = code;
        this.zip = zip;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getZip() {
        return zip;
    }

}
